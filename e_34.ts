// Array of favorite pizza names
let pizzas: string[] = ["Pepperoni pizza", "Margherita pizza", "BBQ chicken pizza"];

// Print pizza names using a for loop
for (let i = 0; i < pizzas.length; i++) {
    console.log(`I like ${pizzas[i]}.`);
}

